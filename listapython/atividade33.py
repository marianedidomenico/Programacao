##Descobrir a area do trapezio 

basemaior = float (input("Insira o valor númerico da base maior do trapézio: "))
basemenor = float (input("Insira o valor númerico da base menor do trapézio: "))
altura = float (input("Insira o valor da altura do trapézio :"))

area = (basemaior+basemenor) * (altura)
area = area/2

print ("A área do trapézio é: ",area)